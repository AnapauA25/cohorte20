//switch case
let dia = prompt("Escribe un numero: ");
switch(dia){
    case 1:
        document.write("Lunes");
        break;
    case 2:
        document.write("martes");
        break;
    case 3:
        document.write("miercoles");
        break;
    case 4:
        document.write("jueves");
        break;
    case 5:
        document.write("viernes");
        break;
    case 6:
        document.write("sabado");
        break;
    case 7:
        document.write("domingo");
        break;
            
}